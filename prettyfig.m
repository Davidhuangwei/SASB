function prettyfig()
% PRETTYFIG set asteatics of figures

set( gca, ...
    'FontName'   , 'Helvetica' ,...
    'FontSize'   , 10          ,...
    'LineWidth'  , 1           );

set( [get(gca,'Title'), get(gca,'XLabel'), get(gca,'YLabel')], ...
    'FontName'   , 'AvantGarde');

set( get(gca,'Title')          , ...
    'FontSize'   , 14          , ...
    'FontWeight' , 'bold'      );

set(findall(gca,'Type','text') , ...
    'FontSize'   ,  10         , ...
    'FontName'   , 'Helvetica' );

set(gca, ...
    'Box'        , 'off'       , ...
    'TickDir'    , 'out'       , ...
    'TickLength' , [.02 .02]   , ...
    'XMinorTick' , 'on'        , ...
    'YMinorTick' , 'on'        , ...
    'XColor'     , [.3 .3 .3]  , ...
    'YColor'     , [.3 .3 .3]  );

% set(gca, 'ColorOrder', ...
%     [ 0.070588,      0.40784 ,     0.70196;...
%       0.92941  ,    0.14118   ,   0.14902;...
%       0.60784   ,    0.7451    ,  0.23922;...
%       0.48235    ,  0.17647      , 0.4549;...
%             1  ,    0.78039     ,       0;...
%       0.30196  ,     0.7451    ,  0.93333;...
%       0.82353   ,    0.4549     ,       0]);

colormap(flipud(gray));

end

